import Foundation

/*
 - Creational Patterns:
 These patterns deal with the process of object creation, providing mechanisms to create objects in a manner suitable to the situation.
 Examples: Singleton, Factory Method, Abstract Factory, Builder, Prototype.
 
 - Structural Patterns:
 These patterns are concerned with the composition of classes or objects, forming larger structures.
 Examples: Adapter, Bridge, Composite, Decorator, Facade, Flyweight, Proxy.
 
 - Behavioral Patterns:
 These patterns focus on the interaction between objects, defining how they communicate and collaborate.
 Examples: Observer, Strategy, Command, Chain of Responsibility, Iterator, State, Template Method, Visitor, Memento.
*/
